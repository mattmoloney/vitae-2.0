                <h2>Red Lentil and Carrot Soup:</h2>
                <br>
                <br>
                <h3>You Will Need:</h3>
                <br>
                <br>
                <ul id="ingredience">
                    <li>1 white onion</li>  
                    <li>2 tsp olive oil</li>
                    <li>3 garlic cloves, sliced</li>
                    <li>2 carrots</li>
                    <li>85g red lentils</li>
                    <li>1 vegetable stock cube, crumbled</li>
                    <li>Generous sprigs of parsley</li>
                </ul>
                <br>
                <br>
                <h3>Method:</h3>
                <br>
                <br>
                <p>Put the kettle on to boil while you finely slice the onion.

Heat the oil in a medium pan, add the onion and fry for 2 mins while you slice the garlic and dice the carrots. Add them to the pan, and cook briefly over the heat.

Pour in 1 litre of the boiling water from the kettle, stir in the lentils and stock cube, then cover the pan and cook over a medium heat for 15 mins until the lentils are tender.

Take off the heat and stir in the parsley. Ladle into bowls, and scatter with extra parsley leaves, if you like.
</p>
                
