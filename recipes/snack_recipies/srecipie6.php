                <h2>Wrap Your Own Spring Rolls:</h2>
                <br>
                <br>
                <h3>You Will Need:</h3>
                <br>
                <br>
                <ul id="ingredience">
                    <li>300g Pack Cooked Rice Noodles from The Chiller Cabinet</li>  
                    <li>About 400g/14oz Mixed Vegetables, Thinly Sliced and Put in Separate Bowls, Such as Red Peppers, Beansprouts, Carrots, Shredded Chinese Leaf Cabbage, Spring Onions</li>
                    <li>140g Cooked Prawns</li>
                    <li>100g Cooked Chicken shredded</li>
                    <li>2 Garlic Cloves, Finely Chopped</li>
                    <li>Small Piece Ginger Finely chopped</li>
                    <li>Splash Light Soy Sauce</li>
                    <li>Chinese Five-Spice Powder, For Sprinkling</li>
                    <li>8-10 Sheets of Brik or Filo Pastry</li>
                    <li>1 Egg Beaten</li>
                    <li>Sesame Seeds, For Sprinkling If You Want</li>
                </ul>
                <br>
                <br>
                <h3>For The Dipping sauce:</h3>
                <br>
                <br>
                <ul id="ingredience">
                    <li>100g Reduced Salt and Sugar Ketchup</li>  
                    <li>1 Tbsp. White Wine Vinegar</li>
                    <li>Small Piece Ginger Grated</li>
                    <li>Pinch of Caster Sugar</li>
                </ul>
                <br>
                <br>
                <h3>Method:</h3>
                <br>
                <br>
                <p>Heat oven to 200C/180C fan/gas 6. Before you get the kids cooking, put the noodles, vegetables, prawns and chicken in individual bowls for everyone to help themselves. Wash hands, put aprons on, sit the kids down and give them their own mixing bowl and spoon. Let them choose which ingredients they want (noodles are essential) in their rolls and if they want to graze as they choose, that’s fine – all the ingredients are cooked or can be eaten raw. Add a bit of garlic and ginger, a tiny dash of soy and sprinkling of five-spice to each bowl and let them mix everything together.
		
Push the bowl aside and lay a sheet of pastry in front of each child. Ask them to spoon the filling down one side of each sheet then give them the beaten egg and a brush so they can brush around the edges. Then help them to roll them up neatly by folding both sides over the filling, then rolling them up.
		
Lift the spring rolls onto a baking tray, seam side down, brush with a little more egg and sprinkle with sesame seeds, if you want. Try to remember which child made which roll to save any arguments at the end! Bake the rolls for 20-25 Mins or until golden.
		
While the rolls are in the oven, make the dipping sauce. Get the kids to mix all the ingredients together until the sugar has dissolved. When the spring rolls are golden and crisp, remove from the oven. Leave until cool enough to handle, cut into pieces for smaller kids, then let them eat, dipping the rolls into the sauce.
</p>
                
