                <h2>Sausage and Sweet Potato Mash:</h2>
                <br>
                <br>
                <h3>You Will Need:</h3>
                <br>
                <h3>For the mash</h3>
                <br>
                <ul id="ingredience">
                    <li>500g sweet potato (cut into chunks)</li>
                    <li>2 tbsp. olive oil</li>
                    <li>Olive oil or a knob of butter</li>
                    <li>Grated nutmeg, to taste (optional)</li>
                </ul> 
                <h3>For the sausages</h3>
                <br>
                <ul id="ingredience">
                     <li>6 large sausage</li>
                     <li>1 tsp West Indian hot pepper sauce</li>
                     <li>juice ½ small lime</li>
                     <li>1 tbsp soft brown sugar</li>
                     <li>2 tsp thyme leaf</li>
                     <li>2 garlic clove, very finely chopped</li>
                     <li>1 tbsp tomato purée</li>
                     <li>Salad, or green beans or peas, to serve</li>  
                </ul>                 
                <br>
                <br>
                <h3>Method:</h3>
                <br>
                <br>
                <p>Heat oven to 220C/200C fan/gas 7. Put the potatoes in a pan of salted water and boil for about 20 mins until soft.
Meanwhile, tip the sausages onto a small baking sheet and bake for 10 mins. While they are cooking, mix the pepper sauce with the lime juice, sugar, thyme, garlic and tomato purée, and stir with 1 tbsp water. When the sausages have had 10-15 mins, brush over the spicy mix and bake for 10 mins more.
Drain the potatoes and mash with oil or butter, and nutmeg to taste, if you like. Serve with salad, or green beans or peas.
</p>
                
