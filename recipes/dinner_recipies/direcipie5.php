                <h2>Ratatoullie:</h2>
                <br>
                <br>
                <h3>You Will Need:</h3>
                <br>
                <br>
                <ul id="ingredience">
                    <li>2 large aubergine</li>  
                    <li>4 small courgette</li>
                    <li>2 red or yellow pepper</li>
                    <li>4 large ripe tomato</li>
                    <li>5 tbsp olive oil</li>
                    <li>Basil</li>
                    <li>1 medium onion</li>
                    <li>3 garlic clove, peeled and crushed</li>
                    <li>1 tbsp red wine vinegar</li>
                    <li>1 tsp sugar</li>
                </ul>
                <br>
                <br>
                <h3>Method:</h3>
                <br>
                <br>
                <p>Cut the aubergines in half lengthways. Place them on the board, cut side down, slice in half lengthways again and then across into 1.5cm chunks. Cut off the courgettes ends, then across into 1.5cm slices. Peel the peppers from stalk to bottom. Hold upright, cut around the stalk, then cut into 3 pieces. Cut away any membrane, then chop into bite-size chunks.
		
Score a small cross on the base of each tomato, then put them into a heatproof bowl. Pour boiling water over the tomatoes, leave for 20 secs, then remove. Pour the water away, replace the tomatoes and cover with cold water. Leave to cool, then peel the skin away. Quarter the tomatoes, scrape away the seeds with a spoon, then roughly chop the flesh.
		
Set a sauté pan over medium heat and when hot, pour in 2 tbsp olive oil. Brown the aubergines for 5 mins on each side until the pieces are soft. Set them aside and fry the courgettes in another tbsp oil for 5 mins, until golden on both sides. Repeat with the peppers. Don’t overcook the vegetables at this stage, as they have some more cooking left in the next step.
		
Tear up the basil leaves and set aside. Cook the onion in the pan for 5 mins. Add the garlic and fry for a further min. Stir in the vinegar and sugar, then tip in the tomatoes and half the basil. Return the vegetables to the pan with some salt and pepper and cook for 5 mins. </p>
                
