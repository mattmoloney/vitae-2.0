                <h2>Beef Casserole:</h2>
                <br>
                <br>
                <h3>You Will Need:</h3>
                <br>
                <br>
                <ul id="ingredience">
                    <li>2 celery</li>  
                    <li>1 onion</li>
                    <li>2 really big carrot</li>
                    <li>5 bay leaf</li>
                    <li>3 sprigs thyme</li>
                    <li>1 tbsp vegetable oil</li>
                    <li>1 tbsp Butter</li>
                    <li>2 tbsp plain flour</li>
                    <li>2 tbsp tomato purée</li>
                    <li>2 tbsp Worcestershire sauce</li>
                    <li>2 beef stock cube, crumbled</li>
                    <li>850g stewing beef (featherblade or brisket works nicely), cut into nice large chunks</li>
                </ul>
                <br>
                <br>
                <h3>Method:</h3>
                <br>
                <br>
                <p>Heat oven to 160C/140C fan/gas 3. Put the kettle on. Put the celery, onion, carrots, bay and 1 thyme sprig in a casserole with 1 tbsp oil and the butter. Soften for 10 mins, then stir in the flour until it doesn’t look dusty anymore, followed by the tomato purée, Worcestershire sauce and beef stock cubes.
		
Gradually stir in 600ml hot water, then tip in the beef and bring to a gentle simmer. Cover and put in the oven for 2hrs 30 mins, then uncover and cook for 30mins – 1hr more until the meat is really tender and the sauce is thickened.
</p>
                
