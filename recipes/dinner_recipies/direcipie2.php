                <h2>Chicken and Chorizo Salad:</h2>
                <br>
                <br>
                <h3>You Will Need:</h3>
                <br>
                <br>
                <ul id="ingredience">
                    <li>3 mild or spicy chorizo sausages, about 4 ounces., cut crosswise into coin-shaped slices, or a 6 ounce piece, cut into chunks</li>
                    <li>12-16 baby zucchini, halved lengthwise, about 10 oz</li>
                    <li>1 lb. boneless, skinless, cooked or smoked chicken, cut or pulled into long strips</li>
                    <li>4 cups loosely packed mixed red and green lettuce leaves</li>
                    <li>1/2 bunch arugula or watercress</li>
                    <li>16-20 black olives, about 3 ounces</li>
                    <li>sprigs of cilantro or flat-leaf parsley</li>
                </ul>
                <br>
                <h3>Vinaigrette</h3>
                <br>
                <ul id="ingredience">
                    <li>2 tablespoons balsamic vinegar</li>
                    <li>1/3 cup extra virgin olive oil</li>
                    <li>2 garlic cloves, crushed</li>
                    <li>kosher salt or sea salt and freshly ground black pepper</li>                    
                </ul>
                <br>
                <br>
                <h3>Method:</h3>
                <br>
                <br>
                <p>Heat a nonstick skillet or stove-top grill pan, add the chorizo slices or chunks, and saut√© gently on all sides until the juices run and the edges are slightly crisp. Set aside.

Mix the vinaigrette ingredients together in a small bowl or pitcher. Use some of the mixture to brush the zucchini halves, then add them to the still-hot pan and cook for 5 minutes on each side or until hot and golden.

Put the chicken into a large salad bowl and add the lettuce leaves and olives. Drizzle over the remaining vinaigrette, then add the chorizo and its juices, and the cilantro or parsley. 

Toss well, then serve immediately with garlic bread, toasted ciabatta, or warmed focaccia.
</p>
                
