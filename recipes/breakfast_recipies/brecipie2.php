                <h2>Avacado on Toast With Chorizo and Fried Eggs:</h2>
                <br>
                <br>
                <h3>You Will Need:</h3>
                <br>
                <br>
                <ul id="ingredience">
                    <li>1 Tbsp. Pumpkin Seed</li>  
                    <li>85g Chorizo</li>
                    <li>1 Large Ripe Avocado</li>
                    <li>½ Lime Juice</li>
                    <li>½ Small Pack Coriander, Chopped</li>
                    <li>2 Egg</li>
                    <li>4 Thick-Cut Slices Whole Meal Bread</li>
                </ul>
                <br>
                <br>
                <h3>Method:</h3>
                <br>
                <br>
                <p>Heat a large frying pan, add the pumpkin seeds and toast for a few Mins until they crack and pop, then tip out into a bowl and set aside. 

Add the chorizo and cook for 5 Mins until it releases some of its oils and becomes crisp. Meanwhile, roughly mash the avocado with the lime juice, half the coriander and a pinch of salt.
		
Heat a griddle pan and cook the bread for a few Mins each side, or pop in the toaster. Push the chorizo to the edge of the frying pan, crack in the eggs and fry to your liking.
		
To serve, divide the toast between 2 plates, spread with the mashed avocado, and pile on the chorizo, fried eggs and pumpkin seeds. Scatter with the remaining coriander.
</p>
                
