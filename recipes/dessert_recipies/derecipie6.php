                <h2>Blackberry Zabaglione:</h2>
                <br>
                <br>
                <h3>You Will Need:</h3>
                <br>
                <br>
                <ul id="ingredience">
                    <li>400g Blackberries</li>
                    <li>140g Golden Caster Sugar</li>
                    <li>6 Egg Yolk</li>
                    <li>1 Vanilla Pod</li>
                    <li>3 Tbsp. Marsala or Sweet Wine</li>
                </ul>
                <br>
                <br>
                <h3>Method:</h3>
                <br>
                <br>
                <p>In a bowl, lightly crush the berries with 25g of the sugar. Add a spoonful to the bottom of 6 small glasses, then set the rest of the berry mixture aside.

Bring some water to a gentle simmer in a medium-size saucepan and tip the egg yolks into a large heatproof bowl. 

Halve and scrape the seeds from the vanilla pod into the egg yolks and add the remaining sugar. Using an electric whisk, beat until light and airy. Splash in the Marsala, place the bowl over the pan of simmering water, then whisk your heart out for 10-12 Mins until the egg yolks are thick and foamy and the whisk leaves a defined trail. 

Drain off some of the juice, then ripple the remaining berries through the mousse and spoon into the glasses.
</p>
                
