                <h2>Tomato-Vegetable Juice:</h2>
                <br>
                <br>
                <h3>You Will Need:</h3>
                <br>
                <ul id="ingredience">
                    <li>1 cup chopped hearts of romaine</li>
                    <li>¼ cup chopped fresh chives</li>
                    <li>2 large tomatoes, cut into wedges</li>
                    <li>¼ fresh jalapeño , stemmed and seeded</li>
                    <li>1 large red bell pepper, cut into eighths</li>
                    <li>2 large stalks celery, trimmed</li>
                    <li>1 medium carrot, peeled</li>
                    <li>Ice cubes (optional)</li>
                </ul> 
